package basicjava;

public class UnaryPlusAndMinusOperator {
    
    public static void main(String[] args) {
        int x = -10;
        int result;
        
        result = +x;
        System.out.println("Out put : "+result);
        
        result = -x;
        System.out.println("Out put : "+result);
    }
 
}
