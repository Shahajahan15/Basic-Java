public class AssignmentOperator {
    public static void main(String[] args) {
        int x = 5;
        int y = 4;
        
        x+=y;  // x = x+y = 9;
        System.out.println("X is : " + x);
        
        x-=y;  // x = x-y = 5;
        System.out.println("X is : " + x);
        
        x*=y;  // x = x*y = 20;
        System.out.println("X is : " + x);
        
        x/=y;  // x = x/y = 5;
        System.out.println("X is : " + x);
        
        x%=y;  // x = x%y = 1;
        System.out.println("X is : " + x);
        
        
    }
}
