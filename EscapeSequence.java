package basicjava;

/**
 * @author Shahajahan \t - Insert a tab in the text at this point. \b - Insert a
 * backspace in the text at this point. \n - Insert a newline in the text at
 * this point. \r - Insert a carriage return in the text at this point. \f -
 * Insert a formfeed in the text at this point. \' - Insert a single quote
 * character in the text at this point. \" - Insert a double quote character in
 * the text at this point. \\ - Insert a backslash character in the text at this
 * point.
 */
public class EscapeSequence {

    public static void main(String[] args) {
        System.out.println("Hello World \nShahajahan");
        System.out.println("\"I am a cse student\".");
        System.out.println("1 \t 2");
        System.out.println("3 \t 4");
    }
}
